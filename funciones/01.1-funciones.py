def holis(nombre, apellido): # Ahora que tiene "nombre" en el argumento, debemos utilizar dicha variable
    print("Hola mundo")
    print(f"bienvenido, {nombre} {apellido}") # <- La variable o parametro

holis("Alex", "brightman") # Cuando llamemos la funcion en este caso, vamos a tener que poner "Nicolas" en el argumentos
# Cuando llamamos a una funcion, lo que esta entre parentesis son argumentos.
# Cuando estamos en la funcion, y estamos utilizando esa funcion, esos son parametros.
# Cuando tenemos mas de un parametro o argumento, se separa por comas.