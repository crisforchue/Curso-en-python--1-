## Comparadores logicos ##

print(1 > 2) #Debe devolver false
print(1 < 2) #Debe devolver true
print(1 >= 2) #Debe devolver false
print(1 <= 2) #Debe devolver true
print(2 <= 2) #Debe devolver true
print(2 == 2) #Debe devolver true, usamos "==" porque "=" es para variables. 
print(2 == "2") #Debe devolver falso, usamos "==" porque "=" es para variables. 
print(2 != '2') #Debe devolver true, != significa desigualdad 